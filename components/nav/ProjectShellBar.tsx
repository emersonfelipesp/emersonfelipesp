const ICONS = {
  github: GitHubIcon,
  pypi: PyPIIcon,
  docker: DockerIcon,
} as const;

export function ProjectShellBar({
  actions,
  stars,
  releases,
  releasesLabel,
  releasesBasePath,
  releasesAllLabel,
}: Props) {
  const [compact, setCompact] = useState(false);

  useEffect(() => {
    if (typeof window === "undefined" || !releases) return;
    let raf = 0;
    function onScroll() {
      cancelAnimationFrame(raf);
      raf = requestAnimationFrame(() => {
        const y = window.scrollY;
        setCompact((prev) => (prev ? y > 4 : y > 8));
      });
    }
    onScroll();
    window.addEventListener("scroll", onScroll, { passive: true });
    return () => {
      window.removeEventListener("scroll", onScroll);
      cancelAnimationFrame(raf);
    };
  }, [releases]);

  if (!actions?.length && !releases && !stars) return null;

  return (
    <div
      className="sticky top-[var(--topnav-h,3.125rem)] z-30 flex items-center justify-end gap-1 border border-t-0 border-border bg-surface/90 px-3 py-1 text-xs backdrop-blur"
    >
      {stars && stars.count !== null ? (
        <a
          href={stars.href}
          target="_blank"
          rel="noopener noreferrer"
          aria-label={`${stars.label} (${stars.count})`}
          title={`${stars.label} (${stars.count})`}
          className="flex items-center justify-center gap-1 p-2 text-muted transition-all duration-150 hover:bg-accent/15 hover:text-accent focus-visible:bg-accent/15 focus-visible:text-accent"
        >
          <StarIcon className="h-4 w-4" />
          <span className="font-mono text-xs tabular-nums">
            {formatStars(stars.count)}
          </span>
        </a>
      ) : null}
      {releases ? (
        <ReleasesDropdown
          releases={releases}
          ariaLabel={releasesLabel}
          basePath={releasesBasePath}
          allLabel={releasesAllLabel}
          compact={compact}
        />
      ) : null}
      {actions?.map((a) => {
        const Icon = ICONS[a.icon];
        return (
          <a
            key={a.icon}
            href={a.href}
            target="_blank"
            rel="noopener noreferrer"
            aria-label={a.label}
            title={a.label}
            className="flex items-center justify-center p-2 text-muted transition-all duration-150 hover:bg-accent/15 hover:text-accent focus-visible:bg-accent/15 focus-visible:text-accent"
          >
            <Icon className="h-4 w-4" />
          </a>
        );
      })}
    </div>
  );
}
